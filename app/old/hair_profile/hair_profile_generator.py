DAMAGE_ADVICE = {
    "Yes": """
Hair has been chemically altered (color, bleach, relaxer, perm, keratin treatment). Chemical processes break disulfide bonds in hair, causing structural damage, increased porosity, and weakness.

**Product Requirements (NON-NEGOTIABLE):**
- **Protein treatments 2-4x per month** to rebuild broken bonds (look for: hydrolyzed keratin, wheat protein, silk protein)
- **Bond-building products** (Olaplex, K18, or similar) to repair disulfide bonds
- **Color-safe, sulfate-free shampoo** to prevent color fade (sulfates strip color molecules)
- **UV protection products** if hair is bleached (sun causes brassiness and further damage)
- **Deep conditioning weekly** with moisturizing mask (protein + moisture balance is critical)
- **Trim every 6-8 weeks** to remove damaged ends and prevent splits from traveling up shaft

**Critical Balance:** Chemically treated hair needs BOTH protein (structure) AND moisture (hydration). Too much protein = brittle, stiff hair. Too much moisture = mushy, weak hair.

**Signs of Protein Overload:** Hair feels straw-like, stiff, breaks easily, tangles more than usual → Do a moisture-only treatment
**Signs of Moisture Overload:** Hair feels gummy, stretches excessively, won't hold style, looks limp → Do a protein treatment
""",
    
    "No": """
Hair is in its natural, virgin state. No chemical damage to manage, which simplifies the routine.

**Product Strategy:**
- Focus on **moisture maintenance** and **preventative care**
- Protein treatments only as needed (1-2x per month for strength, not repair)
- Can use a wider variety of products without worrying about color fade
- Routine can be simpler with fewer corrective treatments

**Advantage:** Your hair has its natural structure intact. Focus on keeping it healthy rather than repairing damage.
"""
}

